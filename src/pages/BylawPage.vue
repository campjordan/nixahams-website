            <div class="article_title">ARTICLE XIV</div>
            <div class="article_subtitle">Notice</div>
            <div class="article_text">Whenever notice is required by this Constitution and Bylaws, it shall be in writing and served upon the party entitled thereto by personal delivery, e-mail, or by placing the notice in an envelope addressed to such party at his or her last known address and depositing the sealed envelope in a United States Post Office mail box with first class postage fully prepaid. When notice is served by mail or e-mail, actual receipt of notice by the party entitled thereto is not necessary for the notice to be effective. Inclusion of any notice in the official newsletter that is mailed or e-mailed to the party entitled to such notice shall be sufficient.

            Proof of service of any required notice may be made by either of the following methods:

            The person serving the notice may file a signed certificate of service with any member of the Executive Committee; or

            If the person serving the notice is a member of the Executive Committee, by the oral statement of such person made at any regular or special meeting of the Members and so noted in the minutes.

            When computing the number of days between the service of a notice and a meeting, the day of personal delivery or mailing of the notice shall not be counted, but every subsequent day, including the day of the meeting, shall be counted.</div>
