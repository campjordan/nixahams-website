        <div class="article_parent">
            <div class="article_title">ARTICLE VII</div>
            <div class="article_subtitle">Directors and Officers</div>
            <div class="article_text">Elected Officers. The Elected Officers of the Corporation shall consist of a President; Vice-President; Treasurer; and Secretary. The Elected Officers of the Corporation shall also constitute the Board of Directors of the Corporation. Any Full Member shall be eligible to election as President, Vice-President, Treasurer or Secretary.

            Election of Officers. Elections of officers whose terms have expired shall be held at a regular or special meeting of the Members in November of each year, or as soon thereafter as may be expedient.

            Terms. Elected Officers shall serve terms consisting of four years. Any Elected Officer may serve unlimited successive terms if so elected by the Full Members. Currently, the term of the President shall expire December 31, 2009, the term of the Vice-President shall expire December 31, 2012, the term of the Treasurer shall expire December 31, 2011, and the term of the Secretary shall expire December 31, 2010.

            Vacancy. In the event an Elected Officer ceases to be a Full Member, resigns, becomes legally incompetent, dies, or is removed from office, such office shall be deemed vacant. In the event of a vacancy in any Elected Office other than President, the unexpired portion of such term shall be filled by appointment of the President, provided that no person shall be appointed to any Elected Office who would not be eligible for election to that office. In the event of a vacancy in the office of the President, the unexpired portion of such term shall be filled by succession in the following order: Vice-President; Treasurer; and Secretary

            Removal. An Elected Officer may be removed from office upon two-thirds vote of the Full Members at any regular or special meeting with at least fourteen days notice to the Members and to the Elected Officer whose removal is to be considered. Such proceedings may be initiated only by a petition signed by either three members of the Executive Committee, or in the alternative by onefifth of the Full Members.</div>
        </div>
        <div class="article_parent">
            <div class="article_title">ARTICLE VIII</div>
            <div class="article_subtitle">Duties of Elected Officers</div>
            <div class="article_text">President. The President shall be the Chief Executive Officer of this Corporation; shall chair all meetings of the Executive Committee and the Members at which he or she is present; may form and dissolve committees, and appoint and remove the chairmen and members thereof; may create and abolish additional offices, and appoint to such offices Full Members, Associate Members, and others, each of whom shall serve at the pleasure of the President; and generally conduct the business of this club subject to law, the Articles of Incoporation, this Constitution and Bylaws, the advice of the Executive Committee, and the will of the Members.

            Vice-President. The Vice-President shall, in the absence of the President, perform the duties of the President; provided that the Vice-President shall not have the power to dissolve committees, to remove chairmen or members from committees, to abolish offices created by the President, to remove officers who serve at the pleasure of the President, to fill vacancies, or to call special meetings of the Executive Committee or the Members.

            Treasurer. The Treasurer shall be the Chief Financial Officer of this club; shall receive all monies due this club, and pay out the same when authorized to do so by the Executive Committee or the Full Members; shall keep the financial books and records of this club; shall report to the Executive Committee, the President, and the Members when requested; and shall file such tax and other financial returns as may be necessary.

            Secretary. The Secretary shall keep minutes of the meetings of the Members, and if requested to do so of the meetings of the Executive Committee; shall be the keeper of all records of the club, except those specifically entrusted to the Treasurer or another by this Constitution and Bylaws, the Executive Committee, the President, or the Members; and shall file such reports as are necessary with the American Radio Relay League and the Secretary of State of the State of Missouri. The Secretary shall also keep the Constitution and Bylaws of the Corporation and have the same with him or her at each meeting of the Executive Committee or Members. The Secretary shall note all amendments, changes, and additions to the Constitution and Bylaws and shall permit the same to be consulted by Members upon request.</div>
