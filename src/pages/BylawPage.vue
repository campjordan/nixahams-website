        <div class="article_parent">
            <div class="article_title">ARTICLE IV</div>
            <div class="article_subtitle">Members</div>
            <div class="article_text">There shall be three categories of membership in this Corporation: Full Members, Associate Members, and Family Memberships.

            Any licensed amateur radio operator shall be eligible to be a Full Member. Any person interested in amateur radio shall be eligible to be an Associate Member. Any Full Member has the option to sign up his or her family for a Family Membership.

            Full Members, Associate Members, and Family Memberships shall have the same rights and benefits of membership, except that Associate Members and Family Members not being a Full Member shall not be eligible to vote, to make or second motions and nominations, or to serve as Elected Officers.

            No person may become a Full Member, Associate Member, or Family Membership until accepted by the Full Members at any regular or special meeting. Dues tendered by any person not accepted into membership shall be returned.

            A Full Member, Associate Member, or Family Membership may be expelled from membership upon two-thirds vote of the Full Members at any regular or special meeting with at least fourteen days notice to the Full Members and to the person whose expulsion is to be considered. Such proceedings may be initiated only by a petition signed by either three members of the Executive Committee, or in the alternative by one-fifth of the Full Members.</div>
        </div>
        <div class="article_parent">
            <div class="article_title">ARTICLE V</div>
            <div class="article_subtitle">Dues</div>
            <div class="article_text">Dues shall be payable annually in advance for the period commencing January 1 and ending December 31. The amount of dues shall be established annually by the Executive Committee. No change in dues shall be effective until the beginning of the next dues period.

            Any Full Member, Associate Member, or Family Membership whose dues are delinquent as of May 1 shall be dropped from membership, provided that such Full Member, Associate Member, or Family Membership may at any time through December 31 reinstate his or her membership by payment of dues without the requirement of again being accepted for membership.

            After July 1, dues for a new Full Member, new Associate Member, or new Family Membership for the partial year remaining shall be one-half of the established annual dues.

            After December 1, dues for a new Full Member, new Associate Member, or new Family Membership for the partial year remaining shall be waived if the new Full Member, Associate Member, or Family Membership pays dues in full for the next year.

            No Full Member, Associate Member, or Family Membership shall be entitled to a refund of dues by virtue of his or her resignation, incompetence, death, or expulsion from membership.</div>
        </div>
        <div class="article_parent">
            <div class="article_title">ARTICLE VI</div>
            <div class="article_subtitle">Meetings of the Members</div>
            <div class="article_text">Members shall meet regularly to conduct the business of this club. A schedule of regular meetings shall be determined from time to time by the Members at any regular or special meeting. If no schedule of regular meetings is in effect, one may be promulgated by the President upon at least fourteen days notice to the Members.

            Special meetings may be called by the President or two members of the Executive Committee acting in concert, upon at least seven days notice to the Members. Any action may be taken at a special meeting that could be taken at a regular meeting.

            A quorum for the conduct of business at any regular or special meeting shall be five Full Members, provided that if two consecutive regular or special meetings have failed due to the lack of a quorum, the requirement of a quorum may be waived at the next regular or special meeting upon at least fourteen days notice to the Members.

            An act of the majority of Full Members present at any regular or special meeting at which a quorum is present or has been waived shall be the act of the Full Members.

            Any action may be taken without a meeting if the Corporation delivers a written ballot to every Full Member entitled to vote on a matter, in accordance with ยง355.266, RSMo.

            No action of the Full Members shall be invalidated because the meeting at which it was taken was not chaired by the President or Vice-President.

            Except when provided otherwise by law, this constitution, or the Full Members, all meetings of the Members shall be governed by Roberts Rules of Order, however in the absence of objection the chairman of the meeting may allow it to proceed informally.
            </div>
