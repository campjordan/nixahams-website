            <div class="article_subtitle">A.R.R.L. Affiliation</div>
            <div class="article_text">It is the policy of this Corporation to be recognized as an American Radio Relay League Affiliated Club.

            The Elected Officers of this Corporation shall use their best efforts to obtain and maintain this Corporation as an American Radio Relay League Affiliated Club.

            It is the policy of this Corporation that membership shall not be denied to any person solely for the purpose of allowing the Corporation to become or remain an American Radio Relay League Affiliated Club.</div>
