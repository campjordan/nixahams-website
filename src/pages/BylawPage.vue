            <div class="article_subtitle">Contracts, Checks & Funds</div>
            <div class="article_text">Contracts.The Executive Committee may authorize any officer(s) or agent(s) of the Corporation, in addition to the Elected Officers authorized by this Constitution and Bylaws, to enter into any contract or execute and deliver any instrument in the name of and on behalf of the Corporation, and such authority may be general or confined to specific instances.

            Checks, Drafts, Etc. All checks, drafts or other orders for payment of money, notes or other evidences of indebtedness issued in the name of the Corporation, shall be signed by such officer(s) or agent(s) of the Corporation and in such manner as set forth in a resolution of the Executive Committee. In the absence of such a resolution, any such instruments shall be signed by at least two Elected Officers of the Corporation.

            Deposits. All funds of the Corporation shall be deposited periodically to the credit of the Corporation in such banks or other depositories as the Executive Committee may direct.

            Audit or Accounting. An audit or accounting may be required by the Executive Committee or the Full Members, to determine that the assets and income of the Corporation are being managed and administered properly.
