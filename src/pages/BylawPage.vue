            <div class="article_title">ARTICLE III</div>
            <div class="article_subtitle">Service Area</div>
            <div class="article_text">The service area of the Corporation shall be Southwest Missouri, but this shall not prohibit the Corporation from participating in any activities outside this service area.</div>
