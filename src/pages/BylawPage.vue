            <div class="article_title">ARTICLE II</div>
            <div class="article_subtitle">Purposes</div>
            <div class="article_text">This Constitution and Bylaws, in conjunction with the Articles of Incorporation, are to serve as the governing documents of the Corporation. The purposes for which the Corporation is formed are:

            Establishing and maintaining an emergency backup communications repeater system for use by the Police and Fire Departments of the City of Nixa, Missouri, in the case of primary system failure.

            Fostering emergency preparedness among amateur radio operators in Southwest Missouri by establishing and maintaining an amateur radio repeater for use in the Skywarn spotting network for the National Weather Service as well as cooperative emergency preparedness efforts with local, state and federal emergency service and disaster

            Providing and assisting in providing emergency communications services for the benefit of the public.

            Providing amateur radio operators in Southwest Missouri with opportunities for fellowship, education, training, operating, and contesting in order to maintain a high level of preparedness, teamwork, and technological expertise to further benefit the public in the event of emergencies.

            Encouraging and improving the amateur service through rules which provide for advancing skills in both the communications and technical phases of the art.

            Expanding the existing reservoir within the amateur radio service of trained operators, technicians and electronics experts by providing education and training opportunities.

            Educating and informing the public about the benefits associated with the amateur radio service.

            Solely for the above purposes, the Corporation is empowered to exercise all rights and powers, including the power to buy, sell, and hold title real and personal property, conferred by the laws of the State of Missouri upon non-profit corporations;

            The Corporation is organized exclusively for charitable, educational, or scientific purposes within the meaning of Section 501(c)(3) of the Internal Revenue Code of 1986, as amended. Notwithstanding any other provision of these Articles, the Corporation shall not carry on any activities not permitted to be carried on: (a) by a corporation exempt from Federal income tax under Section 501(c)(3) of the Internal Revenue Code of 1986, as amended; or (b) by a corporation, contributions to which are deductible under Section 170(c)(2) of the Internal Revenue Code of 1986, as amended; and

            No substantial part of the activities of the Corporation shall consist of lobbying or propaganda or otherwise attempting to influence legislation, and the Corporation shall not participate in or intervene in (including the publishing or distribution of statements) any political campaign on behalf of any candidate for public office.

            The Corporation is not formed for pecuniary or financial gain, and no part of the assets, income, or profit of the Corporation is distributable to, or inures to the benefit of its directors or officers, or other private individual having directly or indirectly, any personal or private interest in the activities of the Corporation; except that the Corporation may pay reasonable compensation for services rendered and may make payments and distributions in furtherance of the non-profit purposes stated herein and in the Articles of Incorporation.
            </div>
