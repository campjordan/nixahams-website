            <div class="article_title">ARTICLE X</div>
            <div class="article_subtitle">Special Powers of the Executive Committee</div>
            <div class="article_text">Whenever it is impractical to present any matter to the Members because:

            The emergency nature of the matter requires action before the next regular meeting of the Members and before a special meeting of the Members could reasonably be called; or

            The pressing nature of the matter requires action before the next regular meeting of the Members, but the matter is of a minor nature rendering it inappropriate to call a special meeting of the Members; or

            The matter was to have been presented to the Members at a regular or special meeting, and that meeting failed due to the lack of a quorum.

            Then the Executive Committee may take any action with respect to such matter as the Members could have taken if it had been presented to them, subject to the following limitations:
            No Full Member, Associate Member, or Family Membership may be expelled;

            No elections may be held;

            No Elected Officer may be removed from office;

            Neither the Articles of Incorporation nor this Constitution and Bylaws may be amended.

            Any action taken by the Executive Committee pursuant to this Article shall be reported to the Members at their next regular or special meeting.</div>
