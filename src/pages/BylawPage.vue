            <div class="article_title">ARTICLE XIII</div>
            <div class="article_subtitle">Alternative Dispute Resolution</div>
            <div class="article_text">In the event an internal dispute arises that cannot be resolved within this Corporation, the dispute will be submitted to an appropriate alternative dispute resolution mechanism of the American Radio Relay League, if one is available. The appropriateness and availability of an alternative dispute resolution mechanism shall be determined solely by the American Radio Relay League.

            For the purpose of this article, an "internal dispute" means any disagreement between two or more Elected Officers, Full Members, or Associate Members, concerning the operation or governance of this Corporation, or the interpretation of the Articles of Incorporation, the Constitution and Bylaws, or any enactment of the Executive Committee or the Members. This definition shall be interpreted liberally as it is the intent of this article to maximize the resolution of disputes within the framework of any available American Radio Relay League alternative dispute resolution mechanism and to minimize the resolution of disputes through traditional litigation.

            The results of the alternative dispute resolution mechanism, if available, shall be final and binding on all Elected Officers, Full Members, Associate Members, and Family Memberships, unless the rules of the alternative dispute resolution mechanism provide otherwise.

            Every Elected Officer, Full Member, Associate Member and Family Membership by accepting and retaining such status agrees to be bound by the provisions of this Article.</div>
