            <div class="article_title">ARTICLE IX</div>
            <div class="article_subtitle">Executive Committee</div>
            <div class="article_text">Composition. The Elected Officers of this Corporation shall also constitute the Executive Committee or Board of Directors.

            Regular Meetings. The Executive Committee may meet in regular session immediately following any regular or special meeting of the Members, without special notice to the members of the Executive Committee. A schedule of additional regular meetings may be determined from time to time by the members of the Executive Committee at any regular or special meeting.

            Special Meetings. Special meetings of the Executive Committee may be called by the President or two members of the Executive Committee acting in concert, without notice or upon whatever notice such convening authority deems appropriate. If notice of the call of a meeting of the Executive Committee is given, a bona fide attempt shall be made to give notice to all members of the Executive Committee, however no action of the Executive Committee shall be invalidated because of a lack of such notice. Any action may be taken at a special meeting that could be taken at a regular meeting.

            Quorum; Voting. A quorum for the conduct of business at any regular or special meeting shall be a majority of the Executive Committee then holding office. Each member of the Executive Committee shall have one vote on each matter that comes before the Executive Committee. An act of the majority of the members of the Executive Committee present at any regular or special meeting at which a quorum is present shall be the act of the Executive Committee.

            No action of the Executive Committee shall be invalidated because the meeting at which it was taken was not chaired by the President or Vice-President. Except when provided otherwise by law, the Articles of Incorporation, this Constitution and Bylaws, or the Executive Committee, all meetings of the Executive Committee shall be governed by Roberts Rules of Order, however in the absence of objection the chairman of the meeting may allow it to proceed informally.</div>
